public class profile {
    private int profileQuantity, profileAge, profileColor;

    public void setProfileQuantity(int profileQuantity)
    {
        this.profileQuantity = profileQuantity;
    }

    public int getProfileQuantity()
    {
        return profileQuantity;
    }

    public void setProfileAge(int profileAge)
    {
        this.profileAge = profileAge;
    }

    public int getProfileAge()
    {
        return profileAge;
    }

    public void setProfileColor(int profileColor)
    {
        this.profileColor = profileColor;
    }

    public int getProfileColor()
    {
        return profileColor;
    }
}
